      body: JSON.stringify(newAlias.value)
    })

    if (!response.ok) {
      throw new Error(`API error: ${response.status}`)
    }

    await loadAlias(true)

    closeCreateModal()

  } catch (error: any) {
    apiError.value = error.message
  } finally {
    isLoading.value = false
  }
}

function closeCreateModal() {
  isCreateModalOpen.value = false
  chunkSearch.value = ''
  newAlias.value = {
    alias_text: '',
    normalized_alias: '',
    document_id: null
  }
}
